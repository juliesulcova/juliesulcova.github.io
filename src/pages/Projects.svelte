      interpreting stories through body movement, with a strongly musical layer, creating a very specific
      theatrical concept. It has no specific storyline. It is an organic theatrical form based on
      the atmosphere of a polyphonic whole, which allows the viewer to find their own stories and
      connections within it.</p>

